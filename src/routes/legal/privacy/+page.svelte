<h2>4. How We Use Information.</h2>
<p>
  All information Alchemy collects about you is used for the purposes outlined
  below.
</p>
<h3>I. To provide our services to you</h3>
<p>
  The information we collect is used to provide the Alchemy app. Your public
  profile data (such as name, bio, etc.) is used to show other members who you
  are so you can make connections with them.
</p>
<p>
  Private data, such as your dating preferences, location, and whether or not
  you're transgender, is used to help filter your and other member's potential
  matches to people you're more likely to be interested in.
</p>
<h3>II. To improve our services</h3>
<p>
  Some data, such as log files, is used for debugging purposes or internal
  research to improve our services. This is to help find bugs and issues with
  the app, and to help determine which features users like or don't like.
</p>
<h3>
  III. To prevent, detect, and fight fraud or other illegal or unauthorized
  activities
</h3>
<p>
  Data we collect about you can be used to help detect and deter malicious or
  illegal activities of you or other members.
</p>
<h2>5. How We Share Information.</h2>
<p>
  Primarily, Alchemy shares your data with other members. This is to allow you
  and other members to make connections with each other.
</p>
<p>
  Alchemy also shares information with our service providers. In most cases, the
  information shared with these service providers is stored securely and only
  accessed by Alchemy. If this is not the case, the data shared with these
  service providers is kept to an absolute minimum.
</p>
<p>
  Alchemy will also share information if it is legally required to, the request
  is reasonably necessary, and the request is about a specific member or
  specific member(s) (that is, Alchemy will not respond to general requests for
  information such as "which members of your platform are _____?").
</p>

<h2 id="rights">6. Your Rights and Choices.</h2>
<p>
  You're in control of your information. Below is a list of the options and
  tools available to you regarding your information:
</p>
<ul>
  <li>
    <b>Profile and account settings in the service.</b>
    The edit profile and settings functions allow you to access, rectify, or remove
    information that you provided to us and that's associated directly to your account
    within the service.
  </li>
  <li>
    <b>Device permissions.</b>
    Mobile platforms can have permission systems for specific types of device data
    and notifications, such as pictures, location services, push notifications, and
    advertising identifiers. You can change your settings on your device to either
    consent or oppose the collection or processing of the corresponding information
    or the display of the corresponding notifications. Doing this may cause certain
    services to lose functionality.
  </li>
  <li>
    <b>Uninstall.</b>
    You can stop all information collection by Alchemy by uninstalling the app. Remember
    that uninstalling the app does not close your account! To close your account,
    please use the corresponding functionality on the service.
  </li>
  <li>
    <b>Account closure.</b>
    You can close your account at any time by using the corresponding functionality
    directly on the service.
  </li>
</ul>
<p>
  You also have the right to access, delete, update, and object to the
  collection of your information. To do any of these, you can contact Alchemy by
  email at <a href="mailto:privacy@usealchemy.app">privacy@usealchemy.app</a>.
  <br />
  Please note that this will also require proof of identity to protect the information
  and privacy of other members.
</p>

